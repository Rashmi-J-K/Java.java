public class practicesetonloops {
    public static void main(String[] args) {

        int sum=0;
        int n=3;
        for(int i=0;i<n;i++)
        {
            sum=(2*i)+sum;
        }
        System.out.println("the sum of even numbers is");
        System.out.println(sum);
    }
}
